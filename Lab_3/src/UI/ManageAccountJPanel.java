    private void btnSearchByAccountNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchByAccountNumberActionPerformed
        // TODO add your handling code here:
        String accountNumber = txtSearchByAccountNumber.getText();
        
        if(accountNumber.isBlank()) {
            JOptionPane.showMessageDialog(null, "Please enter account number");
            return;
        }
        
        Account result = accountDirectory.searchAccount(accountNumber);
        
        if(result == null) {
            JOptionPane.showMessageDialog(null, "Account Number you entered doesn't exist", "warning", JOptionPane.INFORMATION_MESSAGE);
        } else {
            txtSearchByAccountNumber.setText("");
            ViewAccountJPanel viewAccountPanel = new ViewAccountJPanel(UserProcessContainer, result);
            UserProcessContainer.add("ViewAccountJPanel", viewAccountPanel);
            CardLayout layout = (CardLayout) UserProcessContainer.getLayout();
            layout.next(UserProcessContainer);
        }
    }//GEN-LAST:event_btnSearchByAccountNumberActionPerformed

