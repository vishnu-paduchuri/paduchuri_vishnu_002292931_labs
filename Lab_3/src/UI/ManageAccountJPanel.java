    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblAccounts.getModel();
        dtm.setRowCount(0);
        
        for(Account accountInfo: accountDirectory.getAccountList()) {
            Object[] row = new Object[4];
            row[0] = accountInfo;
            row[1] = accountInfo.getRoutingNumber();
            row[2] = accountInfo.getAccountNumber();
            row[3] = accountInfo.getBalance();
            
            dtm.addRow(row);
        }
    }

